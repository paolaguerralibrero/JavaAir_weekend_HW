public class Passenger {

    private String name;
    private int bag;

    public Passenger(String name, int bag) {
        this.name = name;
        this.bag = bag;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getBag() {
        return bag;
    }

    public int setBag(int bag) {
         return this.bag = bag;
    }

}



